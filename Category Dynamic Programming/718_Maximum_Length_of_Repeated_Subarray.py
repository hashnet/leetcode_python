        maxim = 0
        for i in range(len(A)):
            if A[i] == B[0]:
                table[i][0] = 1
                maxim = 1

        for j in range(len(B)):
            if A[0] == B[j]:
                table[0][j] = 1
                maxim = 1

        for i in range(1, len(A)):
            for j in range(1, len(B)):
                if A[i] == B[j]:
                    table[i][j] = 1 + table[i-1][j-1]
                    maxim = max(maxim, table[i][j])

        return maxim

